    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter the amount the customer paid !");
        } else {
            Invoice invoice = new Invoice();
            invoice.setBookingId(idBooking);
            invoice.setDate(ir.getTime());
            invoice.setRedundant(Float.parseFloat(jLabel27.getText() + ""));
            invoice.setTotal((float) this.total);
            invoice.setUserId(this.user.getId());
            InvoiceDAO id = new InvoiceDAO();
            id.saveInvoice(invoice);
            JOptionPane.showMessageDialog(this, "Invoice saved successfully !");
            (new ReceptionistHomeFrm(this.user)).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

