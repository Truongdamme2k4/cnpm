    public InvoiceFrm(User user, int idBooking) {
        initComponents();
        setLocationRelativeTo(null);
        this.user = user;
        this.idBooking = idBooking;
        this.total = 0;
        InvoiceDAO idao = new InvoiceDAO();
        ir = idao.getInvoice(idBooking);
        jLabel3.setText(ir.getCustomerName());
        jLabel5.setText(ir.getCustomerPhone());
        jLabel7.setText((int) (float) ir.getCustomerDebt() + "");
        this.total -= (int) (float) ir.getCustomerDebt();
        jLabel9.setText(ir.getNumberCourt() + "");
        jLabel29.setText((int) (float) ir.getPrice() + "");
        this.total += (int) (float) (ir.getPrice() - ir.getDeposit());
        jLabel11.setText(ir.getTime() + "");
        jLabel13.setText((int) (float) ir.getDeposit() + "");
        jLabel15.setText(ir.getCheckin() + "");
        jLabel17.setText(ir.getCheckout() + "");

        ArrayList<Product> listPoduct = ir.getUsedProduct();
        String[] columnNames = {"No.", "Name", "Count", "Price/1 product"};
        String[][] value = new String[listPoduct.size()][4];
        for (int i = 0; i < listPoduct.size(); i++) {
            value[i][0] = (i + 1) + "";
            value[i][1] = listPoduct.get(i).getName() + "";
            value[i][2] = listPoduct.get(i).getCount() + "";
            value[i][3] = (int) (float) listPoduct.get(i).getPrice() + "";
            this.total += (int) (float) listPoduct.get(i).getPrice() * listPoduct.get(i).getCount();
        }
        DefaultTableModel tableModel = new DefaultTableModel(value, columnNames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                //unable to edit cells
                return false;
            }
        };
        jTable1.setModel(tableModel);
        jLabel22.setText(this.total + "");
    }

