    public EditInvoiceFrm(User user, int idBooking, InvoiceResponse ir) {
        initComponents();
        setLocationRelativeTo(null);
        this.user = user;
        this.idBooking = idBooking;
        this.ir = ir;

        jTextField2.setText(ir.getCustomerName());
        jTextField3.setText(ir.getCustomerPhone());
        jTextField6.setText((int) (float) ir.getCustomerDebt() + "");
        this.total += (int) (float) ir.getCustomerDebt();
        jTextField5.setText(ir.getNumberCourt() + "");
        jTextField7.setText((int) (float) ir.getPrice() + "");
        this.total += (int) (float) (ir.getPrice() - ir.getDeposit());
        jTextField8.setText(ir.getTime() + "");
        jTextField9.setText((int) (float) ir.getDeposit() + "");
        jTextField11.setText(ir.getCheckin() + "");
        jTextField12.setText(ir.getCheckout() + "");

        this.listProduct = ir.getUsedProduct();
        String[] columnNames = {"No.", "Name", "Count", "Price/1 product", ""};
        String[][] value = new String[this.listProduct.size()][5];
        for (int i = 0; i < this.listProduct.size(); i++) {
            value[i][0] = (i + 1) + "";
            value[i][1] = this.listProduct.get(i).getName() + "";
            value[i][2] = this.listProduct.get(i).getCount() + "";
            value[i][3] = (int) (float) this.listProduct.get(i).getPrice() + "";
            value[i][4] = "Delete";
            this.total += (int) (float) this.listProduct.get(i).getPrice() * this.listProduct.get(i).getCount();
        }
        DefaultTableModel tableModel = new DefaultTableModel(value, columnNames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                //unable to edit cells
                return false;
            }
        };
        jTable1.setModel(tableModel);
        jLabel22.setText(this.total + "");

    }

